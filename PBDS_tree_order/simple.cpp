void solve(int caseno){
    int n;
    cin>>n;
    vector<pair<int,int> > pii(n);
    for(int i=0;i<n;i++) {
        int x,y;cin>>x>>y;
        pii[i]={x,y};
    }
    int ans = 0;
    sort(pii.begin(),pii.end());   
    ordered_set pos;
    for(int i=0; i<n; i++) {
        int co= pos.size()-pos.order_of_key(pii[i].second);
        ans+=co;
        pos.insert(pii[i].second);
    
    }
    cout<<ans<<'\n';
    return;
    
}
